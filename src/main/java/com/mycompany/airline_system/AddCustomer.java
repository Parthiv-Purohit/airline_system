    public void AutoID() 
    {
        
        try
        {
            Connection con;
            PreparedStatement pre;
            Class.forName(
            "com.mysql.cj.jdbc.Driver"); 
            
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root","root");
            pre = con.prepareStatement("SELECT MAX(customer_id) from customer");
            ResultSet rs = pre.executeQuery();
            rs.next();
            if(rs.getString("MAX(customer_id)")==null)
            {
                custid.setText("CS001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("MAX(customer_id)").substring(2));
                id++;
                custid.setText("CS" + String.format("%03d",id));
            }
        }
        catch(ClassNotFoundException ex)
        {
             //var obj = Logger.getLogger();
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

