    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            String cid = custid.getText();
            String fname = firstname.getText();
            String lname = lastname.getText();
            String pno = passportno.getText();
            String nid = nationalid.getText();
            String ad = address.getText();
            String cno = contactno.getText();
            String gender = "";
            if(male.isSelected())
            {
                gender = "Male";
            }
            else
            {
                gender = "female";
            }
            DateFormat da = new SimpleDateFormat("yyyy-mm-dd");
            String dob = da.format(date.getDate());
            Connection con;
            PreparedStatement pre;
            Class.forName(
                    "com.mysql.cj.jdbc.Driver"); 
            
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root","root");
            pre = con.prepareStatement("insert into customer(customer_id,firstname,lastname,passportno,nationalid,address,contact_no,gender,dob) values(?,?,?,?,?,?,?,?,?)");
            pre.setString(1,cid);
            pre.setString(2,fname);
            pre.setString(3,lname);
            pre.setString(4,pno);
            pre.setString(5,nid);
            pre.setString(6,ad);
            pre.setString(7,cno);
            pre.setString(8,gender);
            pre.setString(9,dob);
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"Customer Added Successfully");
             this.setVisible(false);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


