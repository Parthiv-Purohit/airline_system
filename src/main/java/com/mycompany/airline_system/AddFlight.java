    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            // TODO add your handling code here:
            String fid = flightid.getText();
            String fname = name.getText();
            String arr = arrival.getText();
            String dept = departure.getText();
            String dur = duration.getText();
            String noofseats = noseats.getText();
            String fare = fares.getText();
            DateFormat da = new SimpleDateFormat("yyyy-mm-dd");
            String dob = da.format(date.getDate());
            
            Connection con;
            PreparedStatement pre;
            Class.forName(
                    "com.mysql.cj.jdbc.Driver"); 
            
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root","root");
            pre = con.prepareStatement("insert into flight(flightid,flightname,arrival,departure,duration,seats,fare,flightdate) values(?,?,?,?,?,?,?,?)");
            pre.setString(1,fid);
            pre.setString(2,fname);
            pre.setString(3,arr);
            pre.setString(4,dept);
            pre.setString(5,dur);
            pre.setString(6,noofseats);
            pre.setString(7,fare);
            pre.setString(8,dob);
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"FLight Added Successfully");
             this.setVisible(false);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


